	isWall: function(pos)
	{
		if (pos.y == 13)
		{
			if (pos.x < 0 || pos.x >= this.map[0].length)
				return false;
		}

		if (pos.x < 0)
			return true;
		else if (pos.x >= this.map[0].length)
			return true;
		if (pos.y < 0)
			return true;
		else if (pos.y >= this.map.length)
			return true;

		return this.map[pos.y][pos.x] == 1;
	},

	getPlayer: function(pos)
	{
		for (var i = 0; i < this.players.length; i++)
		{
			var p = this.players[i];

			if (p.boardPosition.x == pos.x && p.boardPosition.y == pos.y)
				return p;
		}

		return null;
	},

